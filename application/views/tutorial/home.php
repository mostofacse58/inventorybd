<section class="section main-article-chapter" data-menu-title="What is a standard operating procedure (SOP)?">
 <h3 class="section-title"><i class="icon" data-icon="1"></i>What is a standard operating procedure (SOP)?</h3>
 <p>A standard operating procedure is a set of written instructions that describes the step-by-step process that must be taken to properly perform a routine activity. SOPs should be followed the exact same way every time to guarantee that the organization remains consistent and in&nbsp;<a href="https://searchdatamanagement.techtarget.com/definition/compliance">compliance</a>&nbsp;with industry regulations and business standards.</p>
 <p>Standard operating procedures provide the policies, processes and standards needed for the organization to succeed. They can benefit a business by reducing errors, increasing efficiencies and profitability, creating a safe work environment and producing guidelines for how to resolve issues and overcome obstacles.</p>
